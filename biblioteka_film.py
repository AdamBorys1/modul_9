class Film:
    def __init__(self, title, year, genre, number_of_plays):
        self.title = title
        self.year = year
        self.genre = genre
        self.number_of_plays = number_of_plays


class Series:
    def __init__(self, title, year, genre, episode, season, number_of_plays):
        self.title = title
        self.year = year
        self.genre = genre
        self.episode = episode
        self.season = season
        self.number_of_plays = number_of_plays

